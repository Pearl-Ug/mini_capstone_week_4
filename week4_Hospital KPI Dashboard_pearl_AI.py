import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd
import numpy as np

# Reset index and prepare data once
df_reset = df.reset_index()
df_reset['date'] = pd.to_datetime(df_reset['date'])

# -------------------------------
# Create figure with 3 subplots
# -------------------------------
fig, axes = plt.subplots(3, 1, figsize=(14, 18))

# -------------------------------
# 1. BAR CHART: Average Wait Time
# -------------------------------
avg_wait_times = (
    df.groupby('department')['avg_wait_min']
    .mean()
    .sort_values(ascending=False)
)

sns.barplot(
    x=avg_wait_times.index,
    y=avg_wait_times.values,
    ax=axes[0],
    palette='viridis'
)

axes[0].set_title('Average Wait Time by Department')
axes[0].set_xlabel('Department')
axes[0].set_ylabel('Average Wait Time (minutes)')
axes[0].tick_params(axis='x', rotation=45)
axes[0].grid(axis='y', linestyle='--', alpha=0.7)

# -------------------------------
# 2. HEATMAP: Daily Occupancy
# -------------------------------
# Convert to date only (drop time)
df_reset['date_only'] = df_reset['date'].dt.date

daily_occupancy_df = (
    df_reset.groupby(['department', 'date_only'])[['beds_occupied', 'total_beds']]
    .sum()
)

daily_occupancy_df['occupancy_rate'] = (
    daily_occupancy_df['beds_occupied'] / daily_occupancy_df['total_beds']
) * 100

daily_occupancy_df = daily_occupancy_df.reset_index()

# Pivot with just date (no time)
occupancy_map = daily_occupancy_df.pivot(
    index='department',
    columns='date_only',
    values='occupancy_rate'
)

sns.heatmap(
    occupancy_map,
    ax=axes[1],
    annot=True,
    fmt=".1f",
    cmap='YlGnBu',
    linewidths=.5,
    cbar_kws={'label': 'Occupancy Rate (%)'}
)

axes[1].set_title('Daily Bed Occupancy Rate per Department')
axes[1].set_xlabel('Date')
axes[1].set_ylabel('Department')
axes[1].tick_params(axis='x', rotation=45)

# -------------------------------
# 3. LINE CHART: Daily Scan Volume
# -------------------------------
daily_scans = (
    df_reset.groupby('date')['scans_done']
    .sum()
    .reset_index()
)

sns.lineplot(
    x='date',
    y='scans_done',
    data=daily_scans,
    ax=axes[2],
    marker='o'
)

axes[2].set_title('Daily Scan Volume Across All Departments')
axes[2].set_xlabel('Date')
axes[2].set_ylabel('Total Scans Done')
axes[2].grid(True, linestyle='--', alpha=0.7)

