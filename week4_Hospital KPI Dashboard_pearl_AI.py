class Department:
    """
    Represents a single hospital ward/department
    Stores and processes data for one department
    """
    def __init__(self):
        self.df = None
        self.department_data = None

    def load_data(self, file_path):
        self.file_path = file_path
        try:#this helps in error handling
            temp_df = pd.read_csv(self.file_path)
        except FileNotFoundError:
            print(f"Error: File not found at '{self.file_path}'")
            return None
        except Exception as e:
            print(f"Error reading CSV file: {e}")
            return None

        try:
            # Check if 'department' column exists before setting as index
            if 'department' not in temp_df.columns:
                print("Error: 'department' column not found in the CSV file. Cannot set index.")
                return None
            self.df = temp_df.set_index('department')
        except Exception as e:
            print(f"Error setting 'department' as index: {e}")
            return None

        #converting the index column(departments) to lower case
        self.df.index = self.df.index.str.lower()
        #creating a list of departments(without repetitions) in the DataFrame
        departments = self.df.index.unique().tolist()
        # Displaying department options for user to choose from. Use of strip() to remove spaces
        print("Please choose a department from the list below")
        for i, dep in enumerate(departments, 1):
          print(f"{i}. {dep}")

        dep_name = input("Type the name of the department:").strip().lower()


        # Loops through the departments list and checks whether the input is in that list. If the input is not in the list, it prompts the user again for a correct input.
        while dep_name not in departments:
            print('Department not found. Please choose from the displayed options.')
            for i, dep in enumerate(departments, 1):
              print(f"{i}. {dep}")

            dep_name = input("Type the name of the department:").strip().lower()

        try:
            if dep_name in self.df.index:
                self.department_data = self.df.loc[dep_name]
                print(f"Data loaded successfully for {dep_name} department.")
                return self.department_data
            else:
                print(f"Error: Department '{dep_name}' not found in the dataset index after valid selection.")
                return None
        except KeyError:
            print(f"Error: Department '{dep_name}' not found in the dataset index.")
            return None
        except Exception as e:
            print(f"An unexpected error occurred while fetching department data: {e}")
            return None

class Hospital:
    """
    Represents a hospital with multiple departments
    Stores and processes data for multiple departments
    """
    def __init__(self):
        pass

    def calculate_kpis(self, data_df):
        if data_df is None or data_df.empty:
            print("Error: No data provided to calculate KPIs.")
            return None

        KPIS = ['average wait time', 'bed occupancy', 'scan utilization', 'number of departments']
        kpi_choice = input("Please choose a KPI from the list below:\n1. Average wait time \n2. Bed occupancy \n3. Scan utilization \n4. Number of departments \nType the name of KPI: ").lower()

        while kpi_choice not in KPIS:
            print('KPI not found. Please choose from the list provided.')
            kpi_choice = input("Please choose a KPI from the list below:\n1. Average wait time \n2. Bed occupancy \n3. Scan utilization \n4. Number of departments \nType the name of KPI: ").lower()

        try:
            if kpi_choice == 'average wait time':
                if 'avg_wait_min' in data_df.columns:
                    avg_wait_time = data_df['avg_wait_min'].mean()
                    return f'Average wait time = {avg_wait_time:.2f} minutes'
                else:
                    print("Error: 'avg_wait_min' column not found in the provided data.")
                    return None
            elif kpi_choice == 'bed occupancy':
                if 'beds_occupied' in data_df.columns and 'total_beds' in data_df.columns:
                    if (data_df['total_beds'] == 0).any():
                        print("Warning: Some 'total_beds' values are zero. These will be treated as NaN in occupancy calculation.")
                        daily_occupancy = (data_df['beds_occupied'] / data_df['total_beds'].replace(0, np.nan)) * 100
                    else:
                        daily_occupancy = (data_df['beds_occupied'] / data_df['total_beds']) * 100
                    avg_occupancy = daily_occupancy.mean()
                    return f'Average bed occupancy = {avg_occupancy:.2f}%'
                else:
                    print("Error: 'beds_occupied' or 'total_beds' column not found in the provided data.")
                    return None
            elif kpi_choice == 'scan utilization':
                if 'utilization_rate' in data_df.columns:
                    avg_utilization = data_df['utilization_rate'].mean()
                    return f'Scan utilization = {avg_utilization:.2f}'
                else:
                    print("Error: 'utilization_rate' column not found in the provided data.")
                    return None
            elif kpi_choice == 'number of departments':
                # This calculation assumes data_df represents a dataset where department info can be extracted.
                # If data_df is already a subset for a single department, this will likely be 1.
                if 'department' in data_df.columns:
                    num_departments = data_df['department'].nunique()
                elif data_df.index.name == 'department':
                    num_departments = data_df.index.nunique()
                else:
                    num_departments = 1 # Assume if no column/index named 'department', it's a single department's data
                return f'Number of unique departments in the provided data = {num_departments}'
            else:
                print("KPI not found. Please type the name of the KPI correctly as displayed.")
                return None
        except KeyError as ke:
            print(f"Error accessing column for KPI calculation: {ke}")
            return None
        except Exception as e:
            print(f"An unexpected error occurred during KPI calculation: {e}")
            return None


